package com.kh.rightroom.vo;

public class ServiceSetVO {

	private int service_no;
	private int room_no;
	private boolean service_list;
	
	public int getService_no() {
		return service_no;
	}
	public void setService_no(int service_no) {
		this.service_no = service_no;
	}
	public int getRoom_no() {
		return room_no;
	}
	public void setRoom_no(int room_no) {
		this.room_no = room_no;
	}
	public boolean isService_list() {
		return service_list;
	}
	public void setService_list(boolean service_list) {
		this.service_list = service_list;
	}
	
}
